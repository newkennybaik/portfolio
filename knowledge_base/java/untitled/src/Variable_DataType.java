public class Variable_DataType {
    public static void main(String[] args) {
        int age = 33;
        double height = 160.5;
        String name = "백진규";
        boolean isAdult = true;

        System.out.println("이름: " + name);
        System.out.println("나이: " + age);
        System.out.println("키: " + height + "cm");
        System.out.println("성인 여부: " + isAdult);
    }
}